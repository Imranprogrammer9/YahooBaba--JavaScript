// Comparison Operators in JavaScript
// Equal to
var x = 10;
var y = "20";
console.log(x == y);

//Equal Value and Type
var x = 10;
var y = 10;
console.log(x === y);

// Not Equal
var e = 10;
var f = 11;
console.log(e != f);

//Not Equal Value and Type
var g = 100;
var h = 100;
console.log(g !== h);

// Greater Than
var i = 1000;
var j = 100;
console.log(i > j);

// Less Than
var k = 10;
var l = 100;
console.log(k < l);

//Greator than Equal To
var m = 10;
var n = 100;
console.log(m <= n);

// Less than Equal To
var l = 5;
var g = 10;
console.log(l >= g);

// Null and Undefined
console.log(null === undefined);
// Here null and undefined are not equal because they are not the same type.
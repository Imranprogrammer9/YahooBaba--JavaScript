// Declaring & Using Variables in JavaScript
// Declaring a String in JavaScript Variables
var e = "Epinovus Lahore Pakistan";
/* Here var is used as a keyword for declaring variable. "e" is the variable that we have declared. "=" is used as a assignment operator. "Epinovus Lahore Pakistan" is the value assigned to variable "e".*/
// Overriding a variable value in JavaScript
e = 100;
e = 400; //The value entered at last will be shown
e = "Epinovus<br><br>";
document.write(e);

// Declaring a Numeric in JavaScript Variables
var x = 500;
// Remember while declaring numeric values don't use single quotes
document.write(x);

// Declaring variables with singleQuotes
var imranHaider = "<br><br>Imran Haider";
document.write(imranHaider);

// Declaring variables with camelCase
var imranHaider = "<br><br>Imran Haider";
document.write(imranHaider);

// Usisng Multiple Variables in JavaScript
var a, b;
a = "<br><br>Imran Haider<br><br>";
document.write(a);

b = 123;
document.write(b);

// Diiference between var, let and const

//1. Var Keyword
var first_Name = "Imran";
var first_Name = "Haider"
first_Name ="Imran Haider<br><br>"
document.write(first_Name);

//2. Let Keyword
let firstName = "imran"
firstName ="Haider<br>";
document.write(firstName);

//3. Const Keyword
const fName = "<br>Imran";
// fName ="Haider";
document.write(fName);

/* Note Using "var" keyword, you can assign and declare the values but while using "let" keyword you can only assign a value but cannot declare it. Using const Keyword, neither you can declare nor you can assign a value. */

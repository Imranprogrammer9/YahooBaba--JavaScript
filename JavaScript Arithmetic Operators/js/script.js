// Arithmetic Operators in JavaScript

// 1. Addition
var a = 10;
var b = 20;
document.write(a + b);
document.write("<br><br>");

//2. Subtraction
var c = 30;
var d = 40;
document.write(c - d);
document.write("<br><br>");

//3. Multiplication
var e = 50;
var f = 60;
document.write(e * f);
document.write("<br><br>");

//4. Exponentiation
var g = 10;
var h = 5;
document.write(g ** h);
document.write("<br><br>");

//5. Increment
var i = 10;
var j = 11;
// Before Increment
document.write(i + j);
// After Increment
i++;
document.write("<br><br>");
document.write(i + j);

//6. Decrement
var k = 12;
var l = 13;
//Before Decrement
document.write("<br><br>");
document.write(k + i);
// After Decrement
l--;
document.write("<br><br>");
document.write(k + l);

//7. Division
var m = 14;
var n = 2;
document.write("<br><br>");
document.write(m / 2);

//8. Modulus
var o = 4;
